#ifndef _ARCH_H_
#define _ARCH_H_

// basic data type
typedef unsigned char  uint8_t;  // 1 byte
typedef char           sint8_t;  // 1 byte \ 8 bit
typedef unsigned short uint16_t; // 2 byte \ 16 bit
typedef short          sint16_t;
typedef unsigned int   uint32_t; // 4 byte \ 32 bit
typedef int            sint32_t;

#endif
